'''Indentação em Python'''
# A indentação é usada para definir blocos de código em Python.
# Exemplo:
if True:
    print("Este é um bloco de código indentado.")
else:
    print("Este é outro bloco de código indentado.")


'''Estruturas de condição'''
# Condicional if
# Condicional else
# Condicional elif

# Exemplo:
x = 10
if x > 0:
    print("x é positivo")
elif x < 0:
    print("x é negativo")
else:
    print("x é zero")